#operadores aritmeticos
n1=5
n2=2
suma=n1+n2 # suma de dos numeros
resta=n1-n2 # resta de dos numeros
mult=n1*n2 # multiplicacion de dos numeros
div=n1/n2 #division de numero resultado decimal
dive=n1//n2 #division de numero resultado parte entera
pot=n1**n2 # operador potencia
mod=n1%n2 # operador residuo
print(suma)
print(resta)
print(mult)
print(div)
print(dive)
print(pot)
print(mod)
